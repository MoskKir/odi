export async function seedScenarios(dataSource: DataSource): Promise<void> {
  for (const s of scenarios) {
    await dataSource.query(
      `INSERT INTO "scenarios" ("slug", "icon", "title", "subtitle", "description", "difficulty", "published", "requiredBots", "recommendedBots")
       VALUES ($1, $2, $3, $4, $5, $6, $7, $8, $9)
       ON CONFLICT ("slug") DO UPDATE SET
         "icon" = EXCLUDED."icon",
         "title" = EXCLUDED."title",
         "subtitle" = EXCLUDED."subtitle",
         "description" = EXCLUDED."description",
         "difficulty" = EXCLUDED."difficulty",
         "published" = EXCLUDED."published",
         "requiredBots" = EXCLUDED."requiredBots",
         "recommendedBots" = EXCLUDED."recommendedBots"`,
      [s.slug, s.icon, s.title, s.subtitle, s.description, s.difficulty, s.published, s.requiredBots, s.recommendedBots],
    );
  }

  console.log('  -> Scenarios seeded');
}
